namespace ICG.AspNetCore.Utilities.BootstrapTagHelpers;

/// <summary>
///     Represents colors that are supported by bootstrap
/// </summary>
public enum BootstrapColor
{
    /// <summary>
    ///     Represents te primary color for the design
    /// </summary>
    Primary = 1,

    /// <summary>
    ///     Represents the secondary color for the design
    /// </summary>
    Secondary = 2,

    /// <summary>
    ///     Represents a successful outcome, typically green
    /// </summary>
    Success = 3,

    /// <summary>
    ///     Represents a dangerous situation, typically red
    /// </summary>
    Danger = 4,

    /// <summary>
    ///     Represents a warning situation, typically yellow
    /// </summary>
    Warning = 5,

    /// <summary>
    ///     Represents an information element, typically light blue
    /// </summary>
    Info = 6,

    /// <summary>
    ///     Represents a light color
    /// </summary>
    Light = 7,

    /// <summary>
    ///     Represents a dark color
    /// </summary>
    Dark = 8
}