namespace ICG.AspNetCore.Utilities.BootstrapTagHelpers.Form;

/// <summary>
///     How is the form laid out
/// </summary>
public enum FormOrientation
{
    /// <summary>
    ///     Used to represent a form where the label appears above the field
    /// </summary>
    Vertical,

    /// <summary>
    ///     Used to represent a form where the label appears to the left of the field
    /// </summary>
    Horizontal
}